<h3 style="">Historia de 1900</h3>

<p style="text-align: justify;">
    <b>Orígenes (1848 - 1915) </b><br/>
    El fundador del Apostolado de la Oración, Padre Gautrelet, se interesó ya a 
    mediados del siglo XIX por aprovechar el valor apostólico de la oración de 
    los niños, interés que llevó al P. Cros, a formar una agrupación infantil que 
    llamó "Milicia del Papa". Esta agrupación fue incorporada oficialmente al 
    Apostolado de la Oración por el P. Ramiére, quien luego fundaría la primera 
    Cruzada Eucarística para fomentar, no sólo entre jóvenes, sino también entre 
    los adultos, la comunión frecuente. Sin embargo, la Cruzada Eucarística 
    Internacional no surge sino hasta 1914, como un voto del Congreso Eucarístico 
    de Lourdes, para secundar los deseos de la comunión frecuente de los niños, 
    en que fue fundada por Pío X. Desde el primer momento, el Apostolado de la 
    Oración (AO) se ofreció a llevar a la práctica la idea, pero los 
    acontecimientos de la 1ª. Guerra Mundial impidieron realizarla. No obstante, 
    surgen tentativas locales en algunos países de Europa, sobre todo a favor de 
    la paz, y el mismo Benedicto XV, que hizo este llamado durante el conflicto 
    mundial, se consideraba por eso a sí mismo, algunos años después, como 
    "fundador" de la Cruzada. 
    <br/>
    <br/>
    <b>Años de crecimiento y desarrollo (1916 - 1958) </b><br/>
    Después de la 1ª. Guerra Mundial, sobreviene un rápido desarrollo en todo 
    el mundo. En 1932 el día 6 de agosto, día de la festividad de la 
    Transfiguración del Señor, fue promulgada, donde Pío XI eleva a la 
    categoría de "prima primaria" a dos movimientos distintos de Cruzada, el 
    del Apostolado de la Oración, que tendr´ desde entonces su sede en Roma, y 
    el de los PP. Premostratenses de Bélgica, con sede en la Abadía de Abervode 
    (1932). El primer Manual de la Cruzada" con car´cter general se publica 
    durante el tiempo de la Segunda Guerra (1939) y refleja la espiritualidad y 
    la organización de los añs precedentes. Dos Congresos del AO convocados en 
    Roma por el P. Janssens (1948, 1956), sobre todo el segundo, ofrecen material 
    de reflexión sobre lo sucedido en los años anteriores. El Congreso de 1956 
    supone, además, el punto de partida de una reflexión más profunda sobre las 
    "Nuevas Normas" que publicar´ PÍo XII con una cata autógrafa en 1958. 
    <br/>
    <br/>
    <b>El movimiento en Chile </b><br/>
    La Cruzada Eucarística fue traída a Chile en el año 1925 por los Sres. 
    Obispos Edwards y Subercaseaux, desde Francia, como sección infantil del AO. 
    Estuvo en manos, por un corto período, de los padres Asuncionistas y luego 
    la responsabilidad la asumió la Compañía de Jesús, nombrándose como director 
    al P. Ambrosio Martí, quién ejerció el cargo hasta 1958. Posteriormente, la 
    dirección la ejerció el P. Miguel Contardo, hasta 1981 Como en los otros 
    países donde funcionaba, la Cruzada Eucarística experimentá un gran desarrollo 
    hasta antes del Concilio, viédose afectada por la crisis producida tras él. 
    Sin embargo, siempre se la consideró como una escuela de católicos 
    comprometidos y semillero de vocaciones sacerdotales y religiosas. 
    Entre sus logros se cuenta la publicación de la revista "Excelsior", 
    fundada en 1935. 
