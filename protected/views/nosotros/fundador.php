<p style="text-align: justify;">
    <b id="biografia">Padre Luis Fernandez 1925-1990 </b><br/>
    <img src="<?php echo Yii::app()->request->baseUrl; ?>/images/padreLuis.jpg" style="float:right; padding: 20px; padding-top: 0px; width: 250px;" alt="padre Luis Fernandez" />Hijo de una familia de comerciantes. Su padre Luis Fernández Cuñado y su 
    madre María Isabel Carnero Guerrero tuvieron cinco hijos, tres de ellos 
    murieron a temprana edad (uno al nacer, otro en la época de la guerra y el 
    último a la edad de cinco años en la post guerra), por lo cual sólo sobrevive 
    un hermano menor, Pedro, con sus ocho hijos. Nace un día 3 de Octubre de 
    1925 en la ciudad de Valderas, provincia de León al norte de España. 
    Es un pueblo pequeño con no más de 3.000 habitantes dedicados en forma 
    casi exclusiva al trabajo agropecuario (ganadería, oveja merino y cultivo 
    del campo). Esta ciudad, como toda España ha conservado una fuerte tradición 
    religiosa (en Valderas hay tres grandes iglesias). <br/><br/>
    La infancia del padre Luis, son recuerdos de la casa familiar de la calle 
    Ramón u Cajal s/n (nombre de un médico cirujano Premio Nobel). Está ubicada 
    en un costado de la plaza del pueblo con sus característicos portales 
    castellanos. Era el lugar obligado de reunión de todas las fuerzas vivas de 
    la comarca. Se hacía a diario el trato del ganado y de la lana de la 
    semilla y los frutos de la tierra. <br/><br/>
    La plaza del pueblo fue para el pequeño Luis el lugar para sus juegos 
    infantiles y los paseos con su madre. Era la época del gobierno militar, 
    (antes de la guerra) del general Primo de Rivera que España recuerda como 
    difícil y dura. <br/><br/>
    Inicia sus estudios en la Escuela Primaria de Valderas siendo su primera 
    maestra una educadora con un nombre muy singular: doña Fe. Se destaca como 
    el primero de a clase. <br/><br/>
    Es la época también en que su vocación mariana echa sus primeras raíces. 
    Su madre era una mujer de mucha fe y oración, gran devota de la patrona del 
    pueblo, la Virgen Ntra. Sra. del Socorro. Todos los días al atardecer, en la 
    hora del ángelus, rezaba el Santo Rosario junto al pequeño Luis.<br/><br/>
    Esta vocación se ve apoyada e incrementada por otros miembros de la familia 
    ya consagrados: tías y primas religiosas, una de ellas religiosa abadesa de 
    un convento de clausura y su tío Ildefonso, canónigo de la Catedral de 
    Valladolid.<br/><br/>
    Este último siendo párroco en una iglesia en la zona montañosa, invitaba a 
    su sobrino Luis a veranear, quien se sentía feliz de ayudar a diario a su 
    tío como acólito. Contaba ya el pequeño Luis con nueve años de edad y siempre 
    en su vida recordó a su tío con un cariño agradecido.<br/><br/>
    Poco antes del estallido de la guerra civil en España (18 de Julio de 1936) 
    empieza a tener sus primeros contactos con los Hermanos Maristas de Cataluña 
    y de Palencia. Ingresa como internado en las Casa de Formación de la 
    Congregación marista con un sistema de mucha disciplina y rigidez que fue 
    forjando su época pre-juvenil no exenta de intenso estudio y trabajo.<br/><br/>
    A los 14 años, ya en plena guerra civil, viste su primera sotana y el famoso 
    "babero" marista, abrasando así en forma definitiva dicha orden de 
    SanMarcelino Champañat.<br/><br/>
    A la edad de 15 años contrae una de las enfermedades más crueles de la 
    época: tuberculosis. Es el tiempo más duro de la guerra. Las fuerzas 
    republicanas causan estragos en los colegios religiosos. Se vive una 
    época de hambre y escasez de medicamentos. El colegio marista donde 
    estudia el joven Luis Fernández queda al desamparo. Tenía alrededor de 
    1.000 internados y todos los días morían jóvenes de tuberculosis u otra 
    enfermedad. Su madre sin comunicación con el seminariopor la situación 
    bélica que se vive, va en busca de su hijo y lo encuentra en gravísimo 
    estado.<br/><br/>
    Ya en su casa de Valderas, a los cuidados de su madre, deberá soportar 
    un período de diez años la mortal enfermedad que le dejará secuela por 
    el resto de su vida. Gracias a los desvelos maternales y la constante 
    invocación a la Virgen de Ntra. Sra. del Socorro logra salir con vida 
    de la tuberculosis. La alimentación que le prodigaban fue 
    fundamentalmente para ello. En la casa de sus padres había una huerta. 
    Se preparaba el jamón serrano. Era goloso por las tortillas de patatas y el 
    cocido español a base de garbanzos.<br/><br/>
    En estos diez años con diagnóstico médico casi desahuciado, sigue siendo 
    una persona estudiosa y fiel a su vocación. Se interna nuevamente en la 
    Congregación de los hermanos Maristas, con recomendaciones de cuidar su 
    débil salud ante la posibilidad de una recaída. A la edad de 17 años ya 
    daba sus primeras clases en el Colegio Marista de Cataluña.<br/><br/>
    A los 25 años, ya casi recuperado de la enfermedad, conforme al sistema de 
    los Hermanos Maristas de apoyo y misión a los países hispano-americanos, 
    se le ofrece la posibilidad de viajar a Chile. Acepta con agrado, pues 
    siempre comentó que antes de su designación ya había puesto sus ojos "al 
    final de la esquina del mundo".<br/><br/>
    Así en 1950 llega a Chile destinado a cumplir su función educadora de hermano 
    Marista a Los Andes.<br/><br/>
    Las elevadas montañas cordilleranas habrían de impresionar al grupo de 
    jóvenes españoles que llegaban a la ciudad de Los Andes al Colegio Marista 
    "Chacabuco". Entre ellos el joven Luis Fernández quien sabrá en aquella 
    ciudad, pese a su deficiencia física, compartir con entusiasmo, las clases 
    de Religión con la catequesis de niños y jóvenes.<br/><br/>
    Posteriormente la congregación lo envía al colegio "Alonso de Ercilla" de la 
    Gran Avenida, en Santiago. Luego es mandad a Quillota donde permanece 
    cerca de 5 años sirviendo las clases de Religión en el Colegio "Diego Echeverría". 
    A diario debe viajar a la ciudad de La Calera a un Colegio Técnico Rural 
    de la misma orden. Como religioso marista adopta el nombre de "Hermano 
    Pedro". De acuerdo a la costumbre de la época al ingresar un joven a la 
    vida religiosa debía cambiar su nombre de pila. El joven Luis Fernández decide 
    adoptar tal nombre por su hermano mayor.<br/><br/>
    Pero su proyección vocacional iba mucho más allá y decide en 1964 completar 
    sus estudios al sacerdocio. Viaja a Santiago al Seminario Pontificio cuando 
    era rector Monseñor Carlos González Cruchaga. Entre sus compañeros de estudios 
    se cuentan a Monseñor Cristián Prestch, al Padre Miguel Ortega. Su compañero 
    de pieza fue el Padre Oscar Cárdenas.<br/><br/>
    En la ciudad de La Calera es ordenado sacerdote por Monseñor Emilio Tagle 
    Covarrubias el día 21 de noviembre de 1966 (Fiesta de la 
    Presentación de la Virgen).<br/><br/>
    Una de sus primeras misas a ofició a los pies de la Gruta de Lourdes en el 
    Hogar de las Hermanas Hospitalarias de SanJosé en Peñablanca. Quiso 
    con este gesto agradecer a las religiosas el cuidado que le prodigaron en 
    la época en que su salud estuvo resentida.<br/><br/>
    Es enviado en 1967 como Vicario Cooperador en la Parroquia Nuestra Señora 
    del Rosario de Quilpué, siendo párroco Monseñor Carlos Zita, quien le 
    encarga la atención de la abandonada Capilla del Buen Consejo del sector 
    de El Sol, que fue atendida por los padres agustinos. Sólo, invitando 
    personalmente a los vecinos a participar en la Santa Misa, que él oficiaría 
    por primera vez en el lugar, comienza así a vivir los inicios de su intensa 
    labor sacerdotal, que con tiempo se transformará en una obra de evangelización 
    sin precedentes.<br/><br/>
    Su principal anhelo era la formación de laicos. Su gran desvelo honrar y 
    venerar a los sacramentos. Para ello llama a reunión a un pequeño grupo de 
    mujeres para la atención de los niños de Primera Comunión. Surgen las 
    primeras catequistas. Luego forma otro grupo de fieles para la mantención y 
    construcción de las obras parroquiales. Se forma así el Comité Pro-Capilla 
    y Policlínico.<br/><br/>
    Muchos recuerdan de aquella época las "Farras con Cristo". Eran horas de 
    adoración al Santísimo, que por no haber capilla, era expuesto en el living 
    de la casa parroquial.<br/><br/>
    Ya en 1970 se forman en diversos sectores las Primeras Comunidades Eclesiales 
    de Base. Luego este crecimiento se fortalecerá con la creación de las 
    Comunidades Juveniles que se constituirán en semillero de vocaciones 
    religiosas y matrimonios católicos. Llegado el año 1973, viaja a España, 
    para visitar a sus padres y hermano. Regresa en noviembre del mismo año 
    con el deseo de comenzar a gestionar los primeros proyectos para transformar 
    la Comunidad Cristiana de El Sol en Parroquia.<br/><br/>
    Sus anhelos se ven cumplidos cuando el día 15 de agosto de 1975, día de la 
    Asunción de la Virgen, Monseñor Emilio Tagle Covarrubias, erige oficialmente 
    la nueva Parroquia de la diócesis bajo el nombre de "Santa María Madre de 
    la Iglesia".<br/><br/>
    Surge pronto la obra del comedor infantil. Se inicia la catequesis la llamada 
    Catequesis Familiar. Comienzan los estudios y proyectos para la adquisición 
    de un terreno en Colliguay que se transformará en lo que hoy es el Refugio 
    "Santa María", un idílico lugar en medio de las serranías para retiros 
    espirituales y jornadas. También se levanta sobre la antigua capilla el 
    actual templo parroquial.<br/><br/>
    Sin embargo, la época de los 80 fue prodiga en grandes acontecimientos en 
    la vida del padre Luis y sus parroquianos. Su único imperativo era mostrar a 
    Cristo a través del Santísimo Sacramento y una particular devoción a la 
    Santísima Virgen María.<br/><br/>
    Punto de partida de este interés misionero fue la primera semana de abril de 
    1982. Después de una gran misión en todo el sector, el día domingo 2 de 
    abril, es recibida entre santos y alabanzas la imagen venerada de Nuestra 
    Señora de Lo Vásquez, traída desde el Santuario. Durante una semana desfilan 
    ante su presencia niños, jóvenes, ancianos, enfermos, que con su presencia 
    se consagran a Ella, la Madre del Cielo.<br/><br/>
    En 1985 viaja a Roma, invitado a participar en un retiro espiritual que en 
    septiembre inauguraría el Papa Juan Pablo II. Luego visita España, su familia, 
    la tumba de sus padres y tíos. Visita Lourdes, Fátima, El Escorial, 
    Garabandal, Ladeira, etc. Regresa a Chile a continuar su labor, pero ahora 
    con la sotana que le acompañaría hasta su muerte.<br/><br/>
    En1986 se funda el Hogar de Ancianos Santa María. En 1987 viaja nuevamente 
    invitado a Roma a la audiencia pública que concede su Santidad en la Aula 
    Pablo VI. El día miércoles 2 de septiembre logra tener un encuentro personal 
    con el Papa y luego de presentarse solicita la bendición apostólica para su 
    Parroquia. Recibe la bendición papal y además un manojo de rosarios que el 
    Padre Luis posteriormente repartió a los niños de la Parroquia.<br/><br/>
    Regresa a Chile. Pronto un cruel tifus habría de postrarlo un buen tiempo 
    en cama. Se debilitan sus fuerzas.<br/><br/>
    En enero de 1988, ya algo repuesto, parte a la zona sur de Chile invitado por 
    la Armada. Posteriormente ese mismo año viaja a Panamá y a Nueva Orleáns.<br/><br/>
    A fines de 1988 su presencia comienza a verse disminuida ante una fuerte 
    hepatitis que lo obliga a mantener constante reposo. La Parroquia sigue su 
    labor gracias al trabajo tesonero de sus laicos. A comienzos de 1990 su salud 
    se ve tan deteriorada que el día 28 de febrero, día de Miércoles de Cenizas, 
    comienzo de Cuaresma, se interna en el Hospital Naval para realizarse una serie 
    de exámenes clínicos. El diagnóstico pronto es conocido: cáncer hepático.<br/><br/>
    Desconcierto y angustia. Sólo queda la oración y la voluntad de Dios. La 
    noticia se esparce por todo el país. Se inicia una cruzada intensa de oración 
    por su salud.<br/><br/>
    Es enviado a la Parroquia en los días previos a la Semana Santa. Para los 
    numerosos hombres que el día Viernes Santo vivieron el retiro espiritual de 
    ese día, nunca olvidarán la sonrisa que recibieron del Padre Luis cuando 
    dejando por momentos su reposo los fue saludando a cada uno. El día Sábado 
    Santo después de la Misa de la Vigilia Pascual abrió las puertas de su 
    aposento, a sus parroquianos que, con gozo propio de Pascua, fueron a 
    saludarle.<br/><br/>
    Regresa al Hospital Naval. Era la hora de participar en su cuerpo los 
    sufrimientos de Cristo. En la tarde del 19 de mayo, en la hora del 
    ángelus, las campanas se abren al aire y de los cerros, calles y 
    poblaciones, niños y adultos comienzan tristes su peregrinar a la Parroquia. 
    Los Rosarios se elevan al cielo hasta avanzada la madrugada. Al amanecer 
    del frío domingo 20 de mayo las puertas del templo se abren para los 
    fieles que acuden a escuchar la primera Misa del día.<br/><br/>
    En su lecho de moribundo, mientras dos catequistas hacen guardia orando, 
    el buen Padre Luis se va entregando a los brazos de la Madre, la Santísima 
    Virgen María. Duros momentos de fortaleza e instantes de gozo y dolor. En 
    la Parroquia a pocos minutos de las nueve de la mañana, la hora de la Misa, 
    el sacerdote que oficiaría, se prepara para subir al altar cuando llega la 
    triste noticia: ha fallecido el Padre Luis. Ha subido a participar del vino 
    nuevo del Banquete del Reino de los Cielos. Nada tendrá tanto valor como 
    aquellas lágrimas derramadas aquel día. Lágrimas de niños que como pétalos 
    de rosas, era la auténtica muestra del amor inmenso al sacerdote, padre, 
    amigo, maestro que con sello indeleble marcó tantas almas para Cristo.<br/><br/>
    Cada día en su recuerdo elevamos una oración diciendo: "El Señor nos lo dio, 
    el Señor nos lo quitó. Sea bendito el nombre del Señor".<br/><br/>


    <i>Raúl Providel Sanhueza 1990</i>
