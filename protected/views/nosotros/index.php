<h3 style="">Nosotros </h3>

<p style="text-align: justify;">
    La definición tradicional nos dice: "es un ejército juvenil, a las 
    órdenes del Santo Padre, cuyo fin es procurar la santidad 
    propia y la salvación de las almas". Podemos añadir que es un 
    estilo de vida, en que los niños y jóvenes se acercan más a Jesús a 
    través de juegos y reflexiones.
    
    <br/>
    <br/>
    
    <img src="<?php echo Yii::app()->request->baseUrl; ?>/images/CruzadoPenitente.jpg" style="float:right;" />La Cruzada Eucarística está inspirada en las antiguas Cruzadas 
    (siglos XI- XII), que fueron convocadas por el Papa Urbano II, quien muy 
    dolido por la ocupación de Tierra Santa en Palestina, por parte de los 
    Árabes musulmanes; hizo un llamado a los cristianos de Europa para formar 
    un ejército que fuera en rescate de los Santos Lugares, los que estaban 
    siendo destruidos por las hordas paganas. Pero la Cruzada Eucarística que 
    nos interésa, no tiene el mismo objetivo, de las históricas; también 
    queremos rescatar algo muy importante, pero aquí se trata de almas de 
    jóvenes y de niños.
    
    <br/>
    <br/>
    
    En la actualidad, la Cruzada Eucarística de la parroquia Santa María 
    Madre de la Iglesia el Sol, está conformada por alrededor de 50 niños y 
    jóvenes, que se reúnen todos los sábados con el fin de conocer más a 
    Jesús y a María, de ir aprendiendo de sus ejemplos y de los santos 
    patronos, así como disfrutar de una tarde de compartir con sus pares 
    en un ambiente distinto al de los jóvenes actuales, para luego ser 
    luz en sus propios espacios.
    
    <br/>
    <br/>
    
    <b>Todo lo que fue, lo que es y lo que será la Cruzada, nace en la 
        Eucarístia y en el corazón de los niños y jóvenes que forman el 
        ejército de Cristo.</b>
</p>
