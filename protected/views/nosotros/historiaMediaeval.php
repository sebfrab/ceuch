<h3 style="">Historia Medieval</h3>

<p style="text-align: justify;">
   <b>¿ Cómo nacen las Cruzadas ?</b>
   <br/>
   Las cruzadas fueron una serie de campañas militares comúnmente sostenidas por
   motivos religiosos llevadas a cabo por gran parte de la Europa latina 
   cristiana, con el objetivo específico de restablecer el control cristiano de 
   Tierra Santa, se libraron durante un período de casi 200 años, entre 1095 y 
   1921. Las cruzadas fueron sostenidas principalmente contra los musulmanes. 
   Fueron expediciones emprendidas en cumplimiento de un solemne voto para 
   liberar los Lugares Santos de la dominación musulmana. El origen de la 
   palabra se remonta a la cruz hecha de tela y usada como insignia en la ropa 
   exterior de los que tomaron parte en esas iniciativas. 
   <br/>
   <br/>
   <b>Las ocho Cruzadas</b>
   <br/>
   <br/>
   <b>La I cruzada (1095-1099)</b> dirigida por Godofredo de Bouillon, Raimundo IV de 
   Tolosa y Bohemundo I de Tarento culminá con la conquista de Jerusalén (1099), 
   tras la toma de Nicea (1097) y Antioquia (1098), y la formación de los estados 
   latinos en Tierra Santa: el reino de Jerusalén (1099), el principado de 
   Antioquia (1098)y los condados de Edesa (1098) y Trípoli (1199). 
   <br/>
   <br/>
   <b>La II Cruzada (1147-1149)</b> predicada por San Bernardo de Clairvaux tras la 
   toma de Edesa por los turcos, y dirigida por Luis VII de Francia y el 
   emperador Conrado III, terminó con el fracasado asalto a Damasco (1148). 
   <br/>
   <br/>
   <b>La III Cruzada (1189-1192)</b> fue una consecuencia directa de la toma de 
   Jerusalén (1187) por Saladino. Dirigida por Ricardo Corazón de Léon, Felipe 
   II Augusto de Francia y Federico III de Alemania, no alcanzá sus objetivos, 
   aunque Ricardo tomaría Chipre (1191) para cederla luego al Rey de Jerusalén, 
   y junto a Felipe Augusto, Acre (1191) 
   <br/>
   <br/>
   <b>La IV Cruzada (1202-1204)</b> inspirada por Inocencio III ya contra Egipto, 
   terminó desviándose hacia el Imperio Bizantino por la intervención de los 
   venecianos, que la utilizaron en su propio beneficio Tras la toma y saqueo 
   de Constantinopla (1204) se constituyá sobre el viejo Bizancio el Imperio 
   Latino de Occidente, organizado feudalmente y con una autoridad muy débil. 
   Desapareció en 1291 ante la reacción bizantina que constituyeron el llamado 
   Imperio de Nicea, al tiempo que Génova sustituía a Venecia en el control del 
   comercio bizantino. 
   <br/>
   <br/>
   <b>La V (1217-1221) y la VII (1248-1254)</b> Cruzadas, dirigidas por Andrés II de 
   Hungría y Juan de Brienne, y Luis IX de Francia, respectivamente, tuvieron 
   como objetivo el sultanato de Egipto y ambas terminaron en rotundos fracasos. 
   <br/>
   <br/>
   <b>La VIII cruzada (1271)</b> también fue iniciativa de Luis IX. Dirigida contra 
   Túnez concluyó con la muerte de San Luis ante la ciudad sitiada. 
   <br/>
   <br/>
   <b>La VI Cruzada (1228-1229)</b> fue la más extraña de todas, dirigida por un 
   soberano excomulgado, Federico II de Alemania, alcanzó unos objetivos 
   sorprendentes para la época: el condominio confesional de Jerusalén, Belén y 
   Nazareth (1299), status que sin embargo duraría pocos años. 
   <br/>
   <br/>
   <b>Las Cruzadas menores</b><br/>
   Tras el fracaso de la cuarta, el espíritu cruzado se había apagado casi por 
   completo, pese al interés de algunos papas y reyes por reavivarlo. Si los 
   Estados francos sobrevivieron hasta 1291 fue por la intervención de los 
   mongoles que ,al acabar con el califato Abbasí en 1258 y conquistar la región 
   de Oriente Medio, dieron un respiro a los latinos, al no ser los mongoles 
   hostiles al cristianismo.<br/>
   La convicción de que los reiterados fracasos se debían a la falta de inocencia 
   de los cruzados, llevó a la conclusión de que sólo los puros podrían 
   reconquistar Jerusalén. En 1212 un predicador de 12 años organizó la llamada 
   cruzada de los niños, en la que miles de niños y jóvenes recorrieron Francia 
   y embarcaron en sus puertos para ir a liberar Tierra Santa. Fueron capturados 
   por capitanes desaprensivos y vendidos como esclavos. Tan sólo algunos 
   consiguieron regresar al cabo de los años. El cuento era popular en la Edad 
   Media, pero la mayoría de los historiadores creen que este cuento se exagera, 
   o que es un mito.
   <br/>
   <br/>
   <b>Consecuencias </b><br/>
   Las Cruzadas influyeron en múltiples aspectos de la vida medieval, aunque, 
   en general, no cumplieron los objetivos esperados. Casi todas las expediciones 
   militares sufrieron importantes derrotas. Jerusalén se perdería en 1187 y lo 
   que quedó de las posiciones cristianas tras la III Cruzada hasta su definitiva 
   pérdida en el siglo XIII (San Juan de Acre -1291) se limitaba a una estrecha 
   franja litoral cuya pérdida era cuestión de tiempo. Además, los señores de 
   Occidente llevaron sus diferencias tanto a las propias Cruzadas (Luis VII de 
   Francia y Conrado III en la II Cruzada; Ricardo Corazón de León y Felipe II 
   Augusto en la III) como a los estados cristianos fundados en Tierra Santa, 
   dónde los intereses de los diferentes grupos dieron lugar a numerosos conflictos. 
   En el intento de reensamblar las cristiandades latina y griega, no sólo fallá 
   la Cruzada, sino que acentuó el odio y la diferencia entre ellas, 
   convirtiéndose en causa última de la ruptura definitiva entre Roma y 
   Bizancio.<br/>
   Por último, y a pesar de los créditos políticos que las Cruzadas tuvieron 
   para el Papado como director de la política exterior europea, 
   pronto se encontró Roma con voces que criticaban su uso como instrumento 
   al servicio de los intereses papales, sobre todo desde que no se limitaron a 
   los musulmanes, y se dirigieron también contra los disidentes religiosos o 
   los enemigos políticos. 
