<h3 style="">Actualidad</h3>

<p style="text-align: justify;">
    <b>Definición</b><br/>
    La Cruzada Eucarística es un ejército juvenil, a las órdenes del Papa, y 
    cuyo fin es procurar la santidad propia y la salvación de las almas. 
    Actualmente, podemos entenderla como un ejército o Comunidad de niños, 
    jóvenes, asesores que están al servicio del Pasto de la Diócesis, y con 
    fin es trabajar constantemente por el establecimiento del Reino de Jesucristo 
    en las personas, familias y en la sociedad, principalmente con sus armas 
    espirituales, y así procurar la santidad propia en cada uno de sus 
    integrantes. 
    <br/>
    <br/>
    <b>Organización</b>
    está organizada por núcleos, que tienen el nombre de un santo patrono, y 
    están bajo el alero de una Parroquia, Capilla o Colegio. Cada núcleo 
    tiene un Asesor, y sus correspondientes Coordinadores, y cada uno de estos 
    núcleos, está dividido por grupos, bajo las órdenes de un guí. 
    <br/>
    <br/>
    <b>La Cruzada Eucarística en nuestra Parroquia</b><br/>
    La historia de la Cruzada en la Parroquia Santa María Madre de la Iglesia, 
    ubicada en el sector de El Sol, en Quilpué, está intamente relacionada con 
    la fundación de la misma. <br/>
    El Obispo de la Diócesis Don Emilio Tagle C., le encomienda al 
    presbítero Luis Fernandez C., español, que había pertenecido a los 
    Hermanos Maristas, la atención Pastoral del sector dependiendo de la 
    Parroquia Nuestra Señora del Rosario de Quilpué, formando la Comunidad 
    Cristiana de El Sol. <br/>
    Por un decreto del año 1971, firmado por Monseñor Emilio Tagle Covarruvias, 
    se Instituyá la nueva Parroquia, tomando el nombre que hacer honor a la 
    Madre de Dios. <br/>
    El día 15 de agosto de 1971, de feliz memoria , Monseñor Emilio Tagle y su 
    Vicario General Monseñor Carlos Zita, dio inicio a la nueva Parroquia 
    entregándole al Padre Luis Fernández las llaves que lo constituían como 
    Primer Párroco.<br/>
    La comunidad adquiere gran impulso fortaleciéndose los grupos apostólicos, 
    entre ellos, el año 1974, bajo el gran entusiasmo del párroco, fue creada 
    la Cruzada Eucarística, fue él quien siempre tuvo las ganas para que los 
    niños que habían hecho su primera Comunión, y aquellos jóvenes que habían 
    realizado su Confirmación no se alejaran del lado de Jesús, por lo que vió 
    en la Cruzada, una forma de mantenerlos cerca de la Eucaristía, así, con 
    muchas ganas y motivación, apoyá siempre las actividades que se realizaban, 
    estando presente no sólo con la oración sino que él mismo.<br/>
    Nace bajo el amparo de sus cuatro patronos, y se forman núcleos en honor a 
    ellos. San José, San Tarcisio, San Martín de Porres y San Luis de Gonzaga. 
    Teniendo como armas el OSACA: Oración, Sacrificio, Apostolado, Comunión,
    Amor al Papa, a la Virgen y a la Iglesia.<br/>
    Con el paso del tiempo, los distintos núcleos y grupos a lo largo de la 
    diócesis fueron variando en su conformación, para el año 1994, existían 
    21 núcleos, en 11 parroquias y un colegio, existiendo al rededor de 800 
    cruzados participantes, en la región de Valparaíso.<br/>
    La visión del padre Luis, lo lleva a adquirir con la ayuda de la comunidad, 
    para ubicar allí una casa de retiros, en Colliguay conformandose el refugio 
    Santa Maria, de aproximadamente 11.000 metros cuadrados contando actualmente 
    con una capilla, baños artesanales, un pozo ,una cocina y comedor. 
    En esas dependencias, es que los niños y jóvenes han realizado su campamento 
    anual de formación, logrando un crecimiento tanto espiritual como personal, 
    y de lazos entre ellos mismos y con el Señor.<br/>
    En la actualidad, la Cruzada sigue viva y tratando de cumplir su misión 
    fundamental, ser un ejército de Cristo que lucha con el OSACA por rescatar 
    las almas para el Señor. Contamos con el apoyo del párroco, padre Jorge 
    Romero, un Asesor, y con Coordinadores y Guías, dispuestos a trabajar y 
    servir. Jóvenes y niños, alrededor de 50, que con alegría y energía 
    transmiten los valores de la Cruzada a sus pares y familias. Están divididos 
    actualmente en cuatro grupos, niños, premedianos, medianos y grandes, los 
    cuales tienen asignados un santo patrono que los cuida. 
   
